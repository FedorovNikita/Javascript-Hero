// Создавать фкнцию, которая принимает число N и возвращает массив, заполненный
// числа от 1 до N
function getArray(arr) {
    let arrRes = [];

    for (let i = 1; i <= arr; i++) {
        arrRes.push(i);
    }

    return arrRes;
}

