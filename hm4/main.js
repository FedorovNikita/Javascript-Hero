// Создать функцию, которая принимает массив, а возвращает новый массив с 
// дублированными жлементами входного массива
function doubleArray(arr) {
    let a = arr.length;

    for (let i = 0; i < a; i++) {
        arr.push(arr[i]);
    }

    return arr;
}
//console.log(doubleArray([1, 2, 3]));

